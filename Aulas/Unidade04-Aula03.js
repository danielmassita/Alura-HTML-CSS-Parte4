/*
03
Selecionando um elemento específico
PRÓXIMA ATIVIDADE

Usando essa estrutura:

<h2>
<section>
    <h2>
    <p>
    </p>
    <h2>

Como fazemos para selecionar o último <h2>?

A - section ~ h2
Alternativa errada! Desta forma, não selecionamos nenhum dos h2. Não existem h2 irmãos diretos da section.

B - Alternativa correta - section > p + h2
Alternativa correta! O último h2 é filho direto da section e irmão do p.

C- section > h2
Alternativa errada! Desta forma, selecionamos todos os filhos diretos da section, selecionando o primeiro e o último item.
*/